def _demo() -> None:
    """
    Quick smoke-test: run the GA on synthetic demand data and print
    results.  Invoked via `python -m risk_engine.optimizer_ga`.
    """
    # ── configuration ────────────────────────────────────────────────
    N_NODES = 32
    N_SCENARIOS = 200
    config = EVOptimizerConfig(
        pop_size=80,
        max_ports_per_node=20,
        cvar_alpha=0.99,
        max_generations=150,
        convergence_window=25,
        seed=42,
    )

    # ── demand: load Sujay's generated tensor if available ─────────────────
    rng = np.random.default_rng(config.seed)
    tensor_path = os.path.join(os.path.dirname(os.path.dirname(os.path.abspath(__file__))), "output", "extreme_winter_storm.npy")
    if os.path.exists(tensor_path):
        print(f"Using generated tensor: {tensor_path}...")
        demand_scenarios = np.load(tensor_path)
        # Squeeze or format to [S, N] correctly
        if demand_scenarios.ndim == 3: 
            demand_scenarios = demand_scenarios.max(axis=1) # take max across the 24h
        if demand_scenarios.shape[1] > N_NODES:
            demand_scenarios = demand_scenarios[:, :N_NODES]
        elif demand_scenarios.shape[1] < N_NODES:
            demand_scenarios = np.pad(demand_scenarios, ((0, 0), (0, N_NODES - demand_scenarios.shape[1])))
    else:
        demand_scenarios = rng.lognormal(
            mean=3.0, sigma=0.8, size=(N_SCENARIOS, N_NODES)
        )
        # inject a few extreme "black-swan" scenarios
        demand_scenarios[-10:] *= 3.0

    print("=" * 64)
    print("  EVolvAI — ChargerOptimizerGA V1 Demo")
    print("=" * 64)
    print(f"  Nodes: {N_NODES}  |  Scenarios: {N_SCENARIOS}")
    print(f"  Pop: {config.pop_size}  |  Max gens: {config.max_generations}")
    print(f"  CVaR α: {config.cvar_alpha}")
    print("=" * 64 + "\n")

    # ── run ───────────────────────────────────────────────────────────
    ga = ChargerOptimizerGA(config=config, n_nodes=N_NODES)
    result = ga.run(demand_scenarios, verbose=True)

    # ── display best solution ────────────────────────────────────────
    best = result["best_chromosome"]
    print("\n📍 Best chromosome (ports per node):")
    for i, ports in enumerate(best):
        bar = "█" * ports
        print(f"   Node {i:>2}: {ports:>2} ports  {bar}")
    print(f"\n   Total ports: {best.sum()}")


